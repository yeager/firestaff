void v2_tooltip_init(void) {
    memset(&g_tooltip, 0, sizeof(g_tooltip));
}

void v2_tooltip_show(const char* text, int x, int y) {
    if (!text) return;
    strncpy(g_tooltip.text, text, 127);
    g_tooltip.text[127] = '\0';
    g_tooltip.x = x;
    g_tooltip.y = y;
    g_tooltip.visible = true;
    g_tooltip.timer = 3.0f;
    g_tooltip.fade_alpha = 0.0f;
}

void v2_tooltip_hide(void) {
    g_tooltip.visible = false;
    g_tooltip.timer = 0.0f;
    g_tooltip.fade_alpha = 0.0f;
}

void v2_tooltip_update(float dt) {
    if (!g_tooltip.visible) return;
    g_tooltip.timer -= dt;
    if (g_tooltip.timer <= 0.0f) {
        v2_tooltip_hide();
        return;
    }
    if (g_tooltip.timer < 1.0f) {
        g_tooltip.fade_alpha = g_tooltip.timer;
    } else {
        g_tooltip.fade_alpha = 1.0f;
    }
}

void v2_tooltip_render(uint8_t* fb, int w, int h) {
    if (!g_tooltip.visible || !fb || w <= 0 || h <= 0) return;
    int len = (int)strlen(g_tooltip.text);
    int tw = len * 4;
    int th = 5;
    int bx = g_tooltip.x;
    int by = g_tooltip.y;
    int bw = tw + 4;
    int bh = th + 4;

    for (int y = by; y < by + bh && y < h; y++) {
        for (int x = bx; x < bx + bw && x < w; x++) {
            if (x >= 0 && y >= 0) {
                int idx = y * w + x;
                fb[idx] = (uint8_t)(fb[idx] * (1.0f - g_tooltip.fade_alpha) + 40 * g_tooltip.fade_alpha);
            }
        }
    }

    for (int i = 0; i < len; i++) {
        char c = g_tooltip.text[i];
        if (c < 32 || c > 126) continue;
        int ci = c - 32;
        int tx = bx + 2 + i * 4;
        int ty = by + 2;
        for (int row = 0; row < 5; row++) {
            uint8_t bits = FONT_4x5[ci][row];
            for (int col = 0; col < 4; col++) {
                if (bits & (0x08 >> col)) {
                    int px = tx + col;
                    int py = ty + row;
                    if (px >= 0 && px < w && py >= 0 && py < h) {
                        int idx = py * w + px;
                        fb[idx] = (uint8_t)(fb[idx] * (1.0f - g_tooltip.fade_alpha) + 255 * g_tooltip.fade_alpha);
                    }
                }
            }
        }
    }
}

bool v2_tooltip_is_visible(void) {
    return g_tooltip.visible;
}

